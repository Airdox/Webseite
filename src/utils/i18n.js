export const getLocale = () => {
    if (typeof window !== 'undefined' && window.__SITE_LANG__) return window.__SITE_LANG__;
    if (typeof navigator !== 'undefined' && navigator.language?.toLowerCase().startsWith('de')) return 'de';
    return 'en';
};

